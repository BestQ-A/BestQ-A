#!/usr/bin/env node
import { mkdirSync, readFileSync, rmSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..', '..');
const outDir = join(__dirname, 'samples-v2');

const preds = JSON.parse(readFileSync(join(root, 'predictions.json'), 'utf-8'));
const goldList = JSON.parse(readFileSync(
  join(root, 'causal-learner', 'mcp-server', 'src', 'benchmark', 'data', 'swe-bench-lite.json'),
  'utf-8',
));
const usedIndex = JSON.parse(readFileSync(join(root, 'tests', 'mvp-samples', 'index.json'), 'utf-8'));
const usedIds = new Set((usedIndex.selected ?? []).map((item) => item.instance_id));
const goldMap = new Map(goldList.map((item) => [item.instance_id, item]));

const normalized = (text) => String(text ?? '').replace(/\s+/g, ' ').trim();
const similarity = (a, b) => {
  const na = normalized(a);
  const nb = normalized(b);
  if (na === nb) return 1;
  if (!na || !nb) return 0;
  let same = 0;
  const len = Math.min(na.length, nb.length);
  for (let i = 0; i < len; i++) {
    if (na[i] === nb[i]) same++;
  }
  return same / Math.max(na.length, nb.length);
};

const rows = [];
for (const [instanceId, predictedPatch] of Object.entries(preds)) {
  if (usedIds.has(instanceId)) continue;
  if (!predictedPatch || !predictedPatch.trim() || predictedPatch.trim().length < 50) continue;
  const gold = goldMap.get(instanceId);
  if (!gold?.patch) continue;
  const sim = similarity(predictedPatch, gold.patch);
  let verdict = 'partial';
  if (sim >= 0.95) verdict = 'correct';
  else if (sim <= 0.3) verdict = 'wrong';
  rows.push({
    repo: gold.repo,
    instance_id: instanceId,
    problem_statement: gold.problem_statement,
    predicted_patch: predictedPatch,
    gold_patch: gold.patch,
    similarity: Number(sim.toFixed(3)),
    verdict,
  });
}

const wrongPool = rows.filter((row) => row.verdict === 'wrong').sort((a, b) => a.similarity - b.similarity);
const partialPool = rows.filter((row) => row.verdict === 'partial').sort((a, b) => b.similarity - a.similarity);

const selectionState = { ids: new Set(), repos: new Set() };
const takeRows = (pool, count, forcedVerdict) => {
  const picked = [];
  const passes = [
    (row) => row.repo !== 'django/django' && !selectionState.repos.has(row.repo),
    (row) => !selectionState.repos.has(row.repo),
    (row) => row.repo !== 'django/django',
    () => true,
  ];
  for (const predicate of passes) {
    for (const row of pool) {
      if (picked.length >= count) break;
      if (selectionState.ids.has(row.instance_id)) continue;
      if (!predicate(row)) continue;
      selectionState.ids.add(row.instance_id);
      selectionState.repos.add(row.repo);
      picked.push({ ...row, verdict: forcedVerdict ?? row.verdict });
    }
    if (picked.length >= count) break;
  }
  if (picked.length !== count) {
    throw new Error(`Unable to select ${count} rows for verdict=${forcedVerdict ?? 'mixed'}`);
  }
  return picked;
};

const wrong = takeRows(wrongPool, 8, 'wrong');
const correctCandidate = takeRows(partialPool, 3, 'correct_candidate');
const partial = takeRows(partialPool, 4, 'partial');
const selected = [...wrong, ...correctCandidate, ...partial];
const repoDistribution = selected.reduce((acc, row) => {
  acc[row.repo] = (acc[row.repo] ?? 0) + 1;
  return acc;
}, {});

if (Object.keys(repoDistribution).length < 3) {
  throw new Error(`Repo diversity below target: ${Object.keys(repoDistribution).length}`);
}

rmSync(outDir, { recursive: true, force: true });
mkdirSync(outDir, { recursive: true });

const index = {
  generated_at: new Date().toISOString(),
  source_pool: rows.length,
  used_ids_skipped: usedIds.size,
  repo_distribution: repoDistribution,
  selected: [],
};

selected.forEach((row, idx) => {
  const sid = `S${String(16 + idx).padStart(3, '0')}`;
  const sample = {
    id: sid,
    source: 'predictions.json + swe-bench-lite.json',
    repo: row.repo,
    instance_id: row.instance_id,
    problem_statement: row.problem_statement,
    predicted_patch: row.predicted_patch,
    gold_patch: row.gold_patch,
    similarity: row.similarity,
    verdict: row.verdict,
    notes: 'Generated by tests/agent-eval/extract-v2.mjs',
  };
  writeFileSync(join(outDir, `${sid}.json`), JSON.stringify(sample, null, 2));
  index.selected.push({
    id: sid,
    repo: row.repo,
    instance_id: row.instance_id,
    verdict: row.verdict,
    similarity: row.similarity,
  });
});

writeFileSync(join(outDir, 'index.json'), JSON.stringify(index, null, 2));
console.log(`Generated ${selected.length} generalization samples across ${Object.keys(repoDistribution).length} repos.`);
